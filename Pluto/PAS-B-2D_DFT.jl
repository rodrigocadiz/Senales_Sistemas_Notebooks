md"""
Vemos que la 2D-DFT es el resultado de un doble muestreo y periodización, tanto en espacio como en frecuencia, de la Transformada de Fourier bi-dimensional continua. Tal como en el caso uni-dimensional, es posible concebir Transformadas Discretas bi-dimensionales periódicas solo en uno de los dos dominios, espacio o frecuencia. Esto da origen a la DSFT y la 2D-DFFT o también llamada 2D-DFS.
"""

# ╔═╡ a35dcf1d-0599-4f17-8ed6-f0fe665aec13
md"""
## DSFT 

La DSFT (Discrete-space Fourier Transform) se aplica para señales muestreadas en el espacio y periódicas y continuas en frecuencia. Supongamos que $f[m,n]$ ha sido muestreada con un período de muestreo $X$ en la dirección $x$ e $Y$ en la dirección $y$, es decir:

> $$f[m,n] = f(x,y)\rvert_{x=mX,y=nY} = XY f(mX,nY)$$

Entonces, la definición de la DSFT es

!!! note "Definición"
	$$\tilde{F}(u,v) = \sum_{m=-\infty}^{\infty}\sum_{n=-\infty}^{\infty}f[m,n]e^{-i 2 \pi (u mX + v nY)}$$

y la inversa es

!!! note "Definición"
	$$f[m,n] = XY \int_{0}^{1/X}\int_{0}^{1/Y}\tilde{F}(u,v)e^{i 2 \pi (umX + vnY)} dudv$$

porque sabemos que $\tilde{F}(u,v)$ es periódica de periódo $1/X$ en $u$ y $1/Y$ en $v$. 

"""

# ╔═╡ e34951b4-1f3a-44c9-9db0-6acec577db31
md"""
## 2D-DFFT o 2D-DFS


La 2D-DFFT (2D Discrete Frecuency Fourier Transform) o 2D-DFS (2D Discrete Fourier Series) se aplica para señales continuas periódicas en el espacio y muestreadas en frecuencia. Supongamos que $F[k,l]$ ha sido muestreada con un período de muestreo $U$ en la dirección $u$ y $V$ en la dirección $v$, es decir:

> $$F[k,l] = F(u,v)\rvert_{u=kU,v=lV}$$

Entonces, la definición de la 2D-DFFT es

!!! note "Definición"
	$$F[k,l] = UV \int_{0}^{1/U}\int_{0}^{1/V}\tilde{f}(x,y)e^{-i 2 \pi (kUx + lVy)} dxdy$$

porque sabemos que $\tilde{f}(x,y)$ es periódica de período $1/U$ en $x$ y $1/V$ en $y$.

La inversa es 

!!! note "Definición"
	$$\tilde{f}(x,y) = \sum_{k=-\infty}^{\infty}\sum_{l=-\infty}^{\infty}F[k,l]e^{i 2 \pi (kUx + lVy)}$$


"""

# ╔═╡ bf19b6d3-e1e8-4e17-a8a5-67ea9ee6476f
md"""

## MUX = NVY = 1
Recordemos que una señal discreta puede ser periódica solo si el período es un múltiplo entero del periódo o intervalo de muestreo. 

En el muestreo en $x$, el intervalo es $X$ y el período es $1/U$, el inverso del intervalo de muestreo en la frecuencia. Entonces debe cumplirse que

> $MUX = 1$ 

donde $M$ es este múltiplo entero. De manera análoga,

> $NVY = 1$

en el caso de la dimensión $y$, con el múltiplo entero $N$.




Entonces, la 2D-DFT se puede obtener a partir del muestreo en frecuencia de la DSFT mediante

!!! note ""
	$$F[k,l] = \tilde{F}(u,v)\lvert_{u=kU,v=lV} \quad k=0,1,\cdots,M-1 \quad \mathrm{y} \quad l=0,1,\cdots,N-1$$

La 2D-DFT también se puede obtener a partir del muestreo en el espacio de la 2D-DFFT mediante

!!! note ""
	$$f[m,n] = \tilde{f}(x,y)\lvert_{x=mX,y=nY} \quad m=0,1,\cdots,M-1 \quad \mathrm{y} \quad n=0,1,\cdots,N-1$$

"""

