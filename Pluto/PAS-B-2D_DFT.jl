# ╔═╡ 483fb124-cd60-4d75-9c11-30fca4ebae10
md"""
La Transformada de Fourier Discreta en $\mathbb{Z}^2$ o 2D-DFT, se define como

!!! note "Definición"
	$$\tilde{F}[k,l] = \sum_{m=0}^{M-1}\sum_{n=0}^{N-1}\tilde{f}[m,n]e^{-i 2 \pi (\frac{km}{M} + \frac{ln}{N})}$$

Tal como en el caso uni-dimensional, esta Transformada solo opera para señales periódicas. En esta definición $k$ y $l$ son versiones muestreadas de frecuencia espacial.


"""

# ╔═╡ 2f50fb46-1fee-48e2-be26-6984afae0b5c
md"""
La Transformada Inversa es

!!! note "Definición"
	$$\tilde{f}[m,n] = \frac{1}{MN}\sum_{k=0}^{M-1}\sum_{l=0}^{N-1}\tilde{F}[k,l]e^{i 2 \pi (\frac{km}{M} + \frac{ln}{N})}$$

"""

# ╔═╡ bc856073-6d3c-4b65-ad48-96915ee58b9d
md"""
Utilizando la notación de las raíces de la unidad
!!! note ""
	$$W_{N} = e^{-i \frac{2 \pi}{N}}$$

podemos escribir la DFT como

!!! note "Definición"
	$$\tilde{F}[k,l] = \sum_{m=0}^{M-1}\sum_{n=0}^{N-1}\tilde{f}[m,n]W_{M}^{km}W_{N}^{ln}$$

y la inversa como

!!! note "Definición"
	$$\tilde{f}[m,n] = \frac{1}{MN}\sum_{k=0}^{M-1}\sum_{l=0}^{N-1}\tilde{F}[k,l]W_{M}^{-km}W_{N}^{-ln}$$

"""

