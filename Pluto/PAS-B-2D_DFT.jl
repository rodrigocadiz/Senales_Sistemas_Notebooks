# ╔═╡ fbc3e0a8-78c2-4256-9b7d-d1e4b0547eb1
md"""
## Ejemplo

Supongamos la secuencia de ancho y largo finito $(N,N):$ 
> $$f[m,n] = \delta[m-n]\sqcap_{N,N}[m,n]$$

es decir un impulso de línea discreto contenido en un cuadrado.

Utilizando la definición, tenemos

> $$\tilde{F}[k,l] = \sum_{m=0}^{N-1}\sum_{n=0}^{N-1}\delta[m-n] e^{-i 2 \pi (\frac{km}{N} + \frac{ln}{N})}$$

Como solo tenemos valores distintos de cero en la diagonal, solo necesitamos sumar en esos puntos, es decir

> $$\tilde{F}[k,l] = \sum_{m=0}^{N-1}e^{-i 2 \pi (\frac{(k+l)m}{N})}$$

Recordando que las exponenciales complejas son ortogonales, esta suma solo tendrá  el valor $N$ cuando $k+l=0, \pm N, \pm 2N$. Y como la función solo nos interesa en $(N,N)$, entonces,

> $$\tilde{F}[k,l] = N\delta[k+l]$$

A continuación se grafican esta secuencia a la izquierda y su 2D-DFT a la derecha, para el caso $N=32$.

"""

# ╔═╡ da24ded1-8a54-4d37-b40e-241dc39ae32d
begin
	gr(size=(600,300))
	f = Matrix{Int}(I, 32, 32)
	F = fftshift(fft(f))
	pf = heatmap(f,legend=:false,title=L"\delta[m-n]")
	pF = heatmap(abs.(F),legend=:false,title=L"\mathrm{2D-DFT}\{\delta[m-n]\}")
	plot(pf,pF,layout=(1,2))
	
end

# ╔═╡ e2492bd3-ec9c-415d-b933-16c91c454689
md"""
## Separabilidad

Dada la separabilidad de las bases de Fourier, es decir:

!!! note ""
	$$e^{-i 2 \pi (\frac{km}{M} + \frac{ln}{N})} =  e^{-i 2 \pi \frac{km}{M}}e^{-i 2 \pi \frac{ln}{N}}$$

esto implica que la 2D-DFT puede ser calculada mediantes dos DFT uni-dimensionales, primero operando sobre las filas (o columnas) y luego sobre las columnas (o filas) de una imagen o secuencia 2D. 

Es decir

!!! note ""
	$$\begin{align}\mathrm{2D-DFT}\{f[m,n]\} & = \mathrm{1D-DFT}_l\{\mathrm{1D-DFT}_k\{f[m,n]\}\} \\ & = \mathrm{1D-DFT}_k\{\mathrm{1D-DFT}_l\{f[m,n]\}\}\end{align}$$
"""

# ╔═╡ da523b1e-8119-4f74-b43b-f3046aad5a3f
begin
	gr(size=(600,800))
	M=32
	l1 = @layout[a b ; e f; g h]
	pims = []
	im1 = zeros(32,32)
	im1[10:22,10:22] .= .2
	im1[15:17,15:17] .= 1

	#im1[15,4] = 1
	#im1[22,28] = 1
	push!(pims,heatmap(im1,colorbar=nothing,c=:blues,axis=nothing,title=L"f[m,n]",titlefontsize=12))
	fim1 = fftshift(fft(im1))
	push!(pims,heatmap(abs.(fim1),colorbar=nothing,c=:blues,axis=nothing,title=L"\mathrm{2D-DFT}\{f[m,n]\}",titlefontsize=12))
	fim1k = fft(im1,[1])
	push!(pims,heatmap(abs.(fim1k),colorbar=nothing,c=:blues,axis=nothing,title=L"\mathrm{1D-DFT}_{k}\{f[m,n]\}",titlefontsize=12))
	fim1l = fftshift(fft(fim1k,[2]))
	push!(pims,heatmap(abs.(fim1l),colorbar=nothing,c=:blues,axis=nothing,title=L"\mathrm{1D-DFT}_{l}\{\mathrm{1D-DFT}_{k}\{f[m,n]\}\}",titlefontsize=12))
	fim1ll = fft(im1,[2])
	push!(pims,heatmap(abs.(fim1ll),colorbar=nothing,c=:blues,axis=nothing,title=L"\mathrm{1D-DFT}_{l}\{f[m,n]\}",titlefontsize=12))
	fim1kk = fftshift(fft(fim1ll,[1]))
	push!(pims,heatmap(abs.(fim1kk),colorbar=nothing,c=:blues,axis=nothing,title=L"\mathrm{1D-DFT}_{k}\{\mathrm{1D-DFT}_{l}\{f[m,n]\}\}",titlefontsize=12))
	
	
	plot(pims...,layout=l1)	
end


